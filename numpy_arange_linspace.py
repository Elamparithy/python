"""
Examples of arange and linspace in NumPy.
"""

import numpy as np

# arange examples using NumPy

ar1 = np.arange(10)
ar2 = np.arange(1, 10)
ar3 = np.arange(1, 10, 2)
ar4 = np.arange(0.2, 0.8, 0.1)

print('ar1 is', ar1)
print('ar2 is', ar2)
print('ar3 is', ar3)
print('ar4 is', ar4)

# linspace examples using NumPy

ln1 = np.linspace(1, 10)
ln2 = np.linspace(1, 10, 10)
ln3 = np.linspace(1, 10, 5)

print('ln1 is', ln1)
print('ln2 is', ln2)
print('ln3 is', ln3)

